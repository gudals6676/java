
public class Address {
  private String name;
  private int age;
  private String phonenum;
public Address() {
	super();
}
public Address(String name, int age, String phonenum) {
	super();
	this.name = name;
	this.age = age;
	this.phonenum = phonenum;
}
public String getName() {
	return name;
}
public int getAge() {
	return age;
}
public String getPhonenum() {
	return phonenum;
}
  
  
}
